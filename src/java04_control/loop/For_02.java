package java04_control.loop;

public class For_02 {

	public static void main(String[] args) {
		
		
		// i 변수
		// iterate라는 단어의 첫 글자
		// 반복자 == iterator
		for(int i = 0; i < 10; i++) {
			System.out.println(i);  //0~9출력
//			System.out.println(i+1); //1~10출력
			
		}
		// i 변수가 0부터 10보다 작은 동안에 1씩 증가하며 반복
		
		//반복횟수 : 10번
		//반복구간 : 0~9
		
		
		
	}
}
