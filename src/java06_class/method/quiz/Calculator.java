package java06_class.method.quiz;

public class Calculator {

	public int sum(int a,int b) {
		int sum = a+b;
		return sum;
		
	}
	public int sub(int a,int b) {
		int sub = a-b;
		return sub;
		
	}
	
	public int mul(int a,int b) {
		int mul = a*b;
		return mul;
		
	}
	public double div(int a,int b) {
		double div = a/(double)b;
		return div;

	}
	
}
